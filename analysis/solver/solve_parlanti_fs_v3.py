        # MAST L3 NRS1
        if len(w_mast) > 5:
            ax.plot(w_mast, f_mast, color='steelblue', lw=1.5,
                    label=f'MAST L3 {grating} (NRS1)')

        # Our L3 NRS2 raw
        if len(w_nrs) > 5:
            nrs2_lo = NRS2_LO[grating]
            m = w_nrs >= nrs2_lo
            w_e, f_e = w_nrs[m], f_nrs[m]
            ax.plot(w_e, f_e, color='darkorange', lw=1.0, alpha=0.7,
                    label='Our L3 NRS2 ext (raw)')
