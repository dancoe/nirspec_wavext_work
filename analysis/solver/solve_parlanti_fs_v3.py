"""
Parlanti et al. (2025) k/α/β solver — Fixed Slit version 3 (fs_v3)

Upgrade over FS v2
-------------------
FS v2 used per-exposure Level-2 NRS2 x1d from nrs2_spec2_cal/.
FS v3 uses true Level-3 NRS2 x1d from nrs2_spec3_ext/ — products generated by
running Spec3Pipeline (resample_spec + extract_1d) on the Spec2-calibrated cal
files. This provides properly rectified, WCS-resampled spectra in the same
format as MAST Level-3 products. The k/α/β algorithm is identical to v2.

Data pipeline for FS v3 NRS2
------------------------------
  Rate (NRS2) → Spec2 (Parlanti flat/photom overrides, wavecorr skipped)
              → cal.fits → Spec3 (resample_spec + extract_1d)
              → nrs2_spec3_ext/*_x1d.fits

Custom pipeline tweaks applied (WAVEXT.md):
  1. jwst/assign_wcs/nirspec.py: NRS2 M-grating hard-coded error removed
  2. wavelengthrange_0008.asdf: extended range [0.6..3.3] µm for G140M etc.
  3. Parlanti sflat/fflat overrides (NRS2 extended coverage)
  4. Custom photom NRS2 ext table (extrapolated to NRS2 wavelengths)

Algorithm (same as v2)
-----------------------
  Step 1 – k(λ) from median unbiased ratio:
    k_i(λ) = R_i(λ) − α_P(λ)·r₂_i(λ) − β_P(λ)·r₃_i(λ)
    k(λ) = median_i[k_i(λ)], then 40-channel box-smoothed.
    k-sources: P330E (PID 1538), J1743045 (PID 1536).
    G191-B2B excluded: large source-dependent photometric excess at λ > 2.5 µm.

  Step 2 – α̃(λ), β̃(λ) from Parlanti published calibration FITS files.

Comparison
-----------
Validation plots show:
  - MAST L3 NRS1  (gray): nominal MAST Level-3 x1d (no extended coverage)
  - NRS2 raw      (orange): our L3 NRS2 uncorrected spectrum
  - NRS2 corrected (red):  after applying k/α/β
  - Truth (blue dashed): G235M / G395M MAST L3 covering the same wavelengths
  - CALSPEC (dotted black): photometric model

Outputs
--------
  CSV  plots/Parlanti/cal/fs_v3/coeffs_fs_v3_{G140M|G235M}.csv
  PNG  plots/Parlanti/cal/fs_v3/  (coeffs, source spectra, full spectra)
"""
